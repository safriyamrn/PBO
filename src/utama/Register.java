    private int register(java.sql.Connection conn, String nomor_induk, String nama, String username, String password) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("INSERT INTO login (nomor_induk, nama, username, password) VALUES(?, ?, ?, ?)");
        pst.setString(1, nomor_induk);
        pst.setString(2, nama);
        pst.setString(3, username);
        pst.setString(4, password);
        return pst.executeUpdate();
    }

