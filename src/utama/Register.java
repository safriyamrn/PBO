    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed

        String nomor_induk = jTextFieldNomorInduk.getText();
        String nama = jTextFieldNama.getText();
        String username = jTextFieldUsername.getText();
        String password = new String(jPasswordFieldPassword.getPassword());

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ProjectUASPU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();
        Login lg = new Login();

        lg.setNomorInduk(nomor_induk);
        lg.setNama(nama);
        lg.setUsername(username);
        lg.setPassword(password);
        em.persist(lg);
        em.getTransaction().commit();
        em.close();
        
        JOptionPane.showMessageDialog(this, "Login Anda sukses");

                new LoginFrame().setVisible(true);
                this.dispose();
    }//GEN-LAST:event_jButtonRegisterActionPerformed

