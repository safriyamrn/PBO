    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String nim = jTextFieldNIM.getText().trim();
        String nama = jTextFieldNama.getText();
        String jenis_kelamin = jTextFieldJenisKelamin.getText();
        String alamat = jTextFieldAlamat.getText();
        String semester = jTextFieldSemester.getText();
        if (!nim.isEmpty() && !nama.isEmpty() && !jenis_kelamin.isEmpty() && !alamat.isEmpty() && (!semester.isEmpty())) {

            try {
                // TODO add your handling code here:
                Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5433/mahasiswa", "postgres", "bismillah");
                int k = masukkanData(conn, nim, nama, jenis_kelamin, alamat, semester);
                if (k > 0) {
                    tampil(conn);
                    this.peringatan("Simpan Berhasil");
                } else {
                    this.peringatan("Simpan Gagal");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Tampilan.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.peringatan("Input Data Mahasiswa Gagal");
        }
        jTextFieldNIM.setText("");
        jTextFieldNama.setText("");
        jTextFieldJenisKelamin.setText("");
        jTextFieldAlamat.setText("");
        jTextFieldSemester.setText("");

    }//GEN-LAST:event_jButtonSimpanActionPerformed

