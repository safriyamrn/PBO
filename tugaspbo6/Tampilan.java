    private int masukkanData(Connection conn, String nim, String nama, String jenis_kelamin, String alamat, String semester) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("INSERT INTO mahasiswa (nim, nama, jenis_kelamin, alamat, semester) VALUES(?, ?, ?, ?, ?)");
        pst.setString(1, nim);
        pst.setString(2, nama);
        pst.setString(3, jenis_kelamin);
        pst.setString(4, alamat);
        pst.setString(5, semester);
        return pst.executeUpdate();
    }
//update

    private int perbaruiData(Connection conn, String nim, String nama, String jenis_kelamin, String alamat, String semester) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("UPDATE mahasiswa SET nama=?, jenis_kelamin=?, alamat=?,semester=? WHERE nim=?");
        pst.setString(1, nama);
        pst.setString(2, jenis_kelamin);
        pst.setString(3, alamat);
        pst.setString(4, semester);
        pst.setString(5, nim);
        return pst.executeUpdate();
    }
//delete

    private int hapusData(Connection conn, String nim) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("DELETE FROM mahasiswa WHERE nim=?");
        pst.setString(1, nim);
        return pst.executeUpdate();
    }

    private void tampil(Connection conn) {
        dataMahasiswa.clear();
        try {
            String sql = "select * from mahasiswa";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Mahasiswa data = new Mahasiswa();
                data.setNim(String.valueOf(rs.getObject(1)));
                data.setNama(String.valueOf(rs.getObject(2)));
                data.setJenis_kelamin(String.valueOf(rs.getObject(3)));
                data.setAlamat(String.valueOf(rs.getObject(4)));
                data.setSemester(String.valueOf(rs.getObject(5)));
                dataMahasiswa.add(data);
            }
            DefaultTableModel model = (DefaultTableModel) jTableRecord.getModel();
            model.setRowCount(0);
            for (Mahasiswa data : dataMahasiswa) {

                Object[] baris = new Object[5];
                baris[0] = data.getNim();
                baris[1] = data.getNama();
                baris[2] = data.getJenis_kelamin();
                baris[3] = data.getAlamat();
                baris[4] = data.getSemester();

                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Tampilan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

